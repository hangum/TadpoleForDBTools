package com.hangum.tadpole.sql.dao.system;

import java.sql.Date;
import java.sql.Timestamp;

/**
 * schedult result
 * @author hangum
 *
 */
public class ScheduleResultDAO {
	int seq;
	int schedule_main_seq;
	String result;
	String description;
	Date create_time;
	
	public ScheduleResultDAO() {
	}

	public int getSeq() {
		return seq;
	}

	public void setSeq(int seq) {
		this.seq = seq;
	}

	public int getSchedule_main_seq() {
		return schedule_main_seq;
	}

	public void setSchedule_main_seq(int schedule_main_seq) {
		this.schedule_main_seq = schedule_main_seq;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public Date getCreate_time() {
		return create_time;
	}

	public void setCreate_time(Date create_time) {
		this.create_time = create_time;
	}

}
